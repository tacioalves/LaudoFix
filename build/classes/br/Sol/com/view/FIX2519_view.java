    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed

        String validar = jTextFieldNumeroLaudoSGC.getText();
           
        if ((jTextFieldChassi.getText().isEmpty() || jTextFieldChassi.getText() == null) || (jTextFieldNumeroLaudoSGC.getText().isEmpty() || jTextFieldNumeroLaudoSGC.getText() == null) || !validar.matches("[0-9]*")) {
            JOptionPane.showMessageDialog(null, "Campos preenchidos incorretamente");

        } else {
            jTextFieldChassi.setEnabled(false);
            jTextFieldNumeroLaudoSGC.setEnabled(false);
            jButtonConsultar.setEnabled(false);
            try {
                controller.validarLaudo();
            } catch (SQLException ex) {
                Logger.getLogger(FIX2519_view.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_jButtonConsultarActionPerformed

