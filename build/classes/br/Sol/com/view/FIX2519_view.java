    private void jButtonSalvar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvar1ActionPerformed
        if ((jTextFieldNumeroLaudoSGA.getText().isEmpty() || jTextFieldNumeroLaudoSGA.getText() == null) || !jTextFieldNumeroLaudoSGA.getText().matches("\\d{12}") || !jTextFieldNumeroLaudoSGA.getText().matches(("[0-9]*.{12,}"))) {
            JOptionPane.showMessageDialog(null, "Campos preenchidos incorretamente");
        } else if (!jTextFieldNumeroLaudoSGA.getText().matches(("[0-9]*.{12,}")) || !jTextFieldNumeroLaudoSGA.getText().matches("\\d{12}")) {
            JOptionPane.showMessageDialog(null, "Numero Detran incorreto");
        } else {

            int validador = JOptionPane.showConfirmDialog(null, "Numero SGC: " + jTextFieldNumeroLaudoSGC.getText() + "\r\n" + "Chassi: " + jTextFieldChassi.getText() + "\r\n" + "\r\n" + "O numero Detran do laudo será atualizado para: " + "\r\n" + "\r\n" + jTextFieldNumeroLaudoSGA.getText() + "\r\n" + "\r\n" + "Deseja Confirmar Alteração?");

            if (validador == JOptionPane.YES_OPTION) {
                controller.atualizarLaudo();
                Menu_Principal_View menu = new Menu_Principal_View();
                menu.setVisible(true);
                this.setVisible(false);

            } else if (validador == JOptionPane.NO_OPTION) {
                JOptionPane.showMessageDialog(null, "Registro não foi alterado");

            } else if (validador == JOptionPane.CANCEL_OPTION) {
                Menu_Principal_View menu = new Menu_Principal_View();
                menu.setVisible(true);
                this.setVisible(false);
            }
        }


    }//GEN-LAST:event_jButtonSalvar1ActionPerformed

